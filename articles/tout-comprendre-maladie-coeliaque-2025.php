    <p>La maladie coeliaque touche environ 1% de la population mondiale, mais une grande majorite des personnes atteintes ne le savent pas encore. Souvent confondue avec une simple intolerance alimentaire, elle est en realite une maladie auto-immune serieuse qui necessite une prise en charge specifique.</p>

    <h2>Qu est-ce que la maladie coeliaque exactement ?</h2>
    <p>La maladie coeliaque (ou coeliaquie) est une maladie auto-immune declenchee par l ingestion de gluten chez des personnes genetiquement predisposees. Contrairement a une simple intolerance, il ne s agit pas d une reaction de sensibilite : le systeme immunitaire reagit de maniere excessive en attaquant les villosites intestinales, ces petites structures en forme de doigts qui tapissent l intestin grele et permettent l absorption des nutriments.</p>
    <p>Avec le temps, cette inflammation chronique entraine une atrophie des villosites, rendant l absorption des nutriments (fer, calcium, vitamines B12 et D notamment) de plus en plus difficile. Resultat : des carences parfois severes, meme chez des personnes qui mangent normalement.</p>

    <h2>Les symptomes : bien plus varies qu on ne le croit</h2>
    <p>C est la que la maladie coeliaque est particulierement trompeuse. Beaucoup de gens imaginent qu elle se manifeste uniquement par des douleurs abdominales et des diarrhees.</p>
    <p>En realite, la liste des symptomes est tres longue et inclut :</p>
    <ul>
        <li>Manifestations digestives classiques (ballonnements, diarrhees, constipation, nausees)</li>
        <li>Fatigue chronique intense</li>
        <li>Anemie inexpliquee</li>
        <li>Douleurs articulaires</li>
        <li>Aphtes recurrents</li>
        <li>Dermatite herpetiforme (eruption cutanee caracteristique)</li>
        <li>Troubles neurologiques</li>
        <li>Depression</li>
        <li>Chez les enfants, un retard de croissance</li>
    </ul>
    <p>La forme dite "silencieuse" existe aussi : certaines personnes n ont aucun symptome apparent mais presentent des lesions intestinales detectables.</p>

    <h2>Comment se fait le diagnostic en 2025 ?</h2>
    <p>Le diagnostic repose sur deux etapes complementaires :</p>
    <ol>
        <li>Une analyse sanguine recherche des anticorps specifiques, notamment les anticorps anti-transglutaminase de type IgA (anti-tTG IgA), qui sont tres fiables.</li>
        <li>Une biopsie de l intestin grele lors d une endoscopie digestive haute confirme le diagnostic en visualisant l etat des villosites.</li>
    </ol>
    <p><strong>Important :</strong> Il est absolument essentiel de ne pas commencer le regime sans gluten AVANT le diagnostic : en l absence de gluten dans l alimentation, les tests peuvent revenir negatifs alors que la maladie est bien presente.</p>

    <h2>Maladie coeliaque vs intolerance vs sensibilite</h2>
    <p>Ces trois termes sont souvent confondus, mais ils decrivent des realites tres differentes :</p>
    <ul>
        <li><strong>Maladie coeliaque :</strong> maladie auto-immune genetique, permanente, avec des lesions intestinales mesurables.</li>
        <li><strong>Allergie au ble :</strong> reaction allergique immediate (IgE-mediee), qui peut entrainer des reactions anaphylactiques graves.</li>
        <li><strong>Sensibilite non-coeliaque au gluten (SNCG) :</strong> les symptomes ressemblent a ceux de la coeliaquie, mais sans les anticorps caracteristiques ni les lesions intestinales.</li>
    </ul>

    <h2>Le traitement : un regime strict a vie</h2>
    <p>A ce jour, le seul traitement efficace de la maladie coeliaque reste le regime sans gluten strict et a vie. Il n existe pas de medicament permettant de "guerir" la maladie.</p>
    <p>La bonne nouvelle : avec un regime bien suivi, les villosites intestinales se regenerent progressivement (en 1 a 2 ans en general), les carences se resorbent et la qualite de vie s ameliore considerablement.</p>

    <h2>Vivre avec la maladie coeliaque au quotidien</h2>
    <p>Le diagnostic peut sembler devastateur au depart. La bonne nouvelle : on apprend tres vite a vivre sans gluten, et le regime devient une seconde nature. Les restaurants proposent de plus en plus de menus adaptes, les produits sans gluten sont partout en grande surface, et la communaute coeliaque est tres active et solidaire.</p>
    
