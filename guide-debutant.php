    <p>Le gluten n est pas une substance unique : c est un ensemble de proteines, principalement la gliadine et la glutenine, que l on trouve naturellement dans certaines cereales. Ces proteines donnent a la farine sa capacite a former une pate elastique et moelleuse.</p>
