<div class="recipe-body">
    <div class="ingredients-box">
        <h3>Ingredients</h3>
        <ul>
            <li>200 g de farine de sarrasin</li>
            <li>150 g de farine de riz complete</li>
            <li>100 g de fecule de pomme de terre</li>
            <li>50 g de graines de psyllium</li>
            <li>1,5 c. a cafe de sel</li>
            <li>1 sachet de levure boulanger seche (7 g)</li>
            <li>1 c. a cafe de sucre</li>
            <li>350 ml d eau tiede</li>
            <li>2 c. a soupe d huile d olive</li>
            <li>1 oeuf</li>
            <li>Graines de tournesol (optionnel)</li>
        </ul>
    </div>
    <div class="steps">
        <h3>Etapes de preparation</h3>
        <div class="step">
            <div class="step-num">1</div>
            <div class="step-content">Dans un petit bol, melangez la levure avec le sucre et 100 ml d eau tiede. Laissez reposer 10 minutes jusqu a ce que le melange mousse.</div>
        </div>
        <div class="step">
            <div class="step-num">2</div>
            <div class="step-content">Dans un grand bol, melangez les farines, la fecule, le psyllium et le sel.</div>
        </div>
        <div class="step">
            <div class="step-num">3</div>
            <div class="step-content">Ajoutez le melange levure, l huile d olive, l oeuf et le reste d eau tiede. Melangez vigoureusement avec une cuillere en bois ou au robot.</div>
        </div>
        <div class="step">
            <div class="step-num">4</div>
            <div class="step-content">La pate sera plus epaisse et collante qu une pate a pain classique, c est tout a fait normal avec les farines sans gluten.</div>
        </div>
        <div class="step">
            <div class="step-num">5</div>
            <div class="step-content">Transferez la pate dans un moule a cake (25 cm) legerement huile et tapisse de papier cuisson.</div>
        </div>
        <div class="step">
            <div class="step-num">6</div>
            <div class="step-content">Lissez le dessus avec une spatule humide. Saupoudrez de graines si vous le souhaitez.</div>
        </div>
        <div class="step">
            <div class="step-num">7</div>
            <div class="step-content">Couvrez d un torchon propre et laissez lever dans un endroit chaud pendant 45 a 60 minutes.</div>
        </div>
        <div class="step">
            <div class="step-num">8</div>
            <div class="step-content">Prechauffez le four a 220°C. Enfournez le pain et baissez immediatement a 200°C.</div>
        </div>
        <div class="step">
            <div class="step-num">9</div>
            <div class="step-content">Faites cuire 45 a 50 minutes. Le pain est cuit quand il sonne creux en tapant dessous et que la croute est bien doree.</div>
        </div>
        <div class="step">
            <div class="step-num">10</div>
            <div class="step-content">Laissez refroidir completement sur une grille avant de trancher. C est important : un pain sans gluten coupe chaud peut sembler pas cuit a l interieur.</div>
        </div>
