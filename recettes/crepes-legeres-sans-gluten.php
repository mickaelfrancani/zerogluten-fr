        <div class="step">
            <div class="step-num">1</div>
            <div class="step-content">Dans un grand bol, melangez la farine de riz, la fecule, le sel et le sucre vanille.</div>
        </div>
        <div class="step">
            <div class="step-num">2</div>
            <div class="step-content">Faites un puits au centre et cassez les oeufs dedans.</div>
        </div>
        <div class="step">
            <div class="step-num">3</div>
            <div class="step-content">Commencez a melanger en incorporant la farine depuis les bords vers le centre.</div>
        </div>
        <div class="step">
            <div class="step-num">4</div>
            <div class="step-content">Ajoutez le lait progressivement en fouettant pour eviter les grumeaux. La pate doit etre lisse et fluide.</div>
        </div>
        <div class="step">
            <div class="step-num">5</div>
            <div class="step-content">Ajoutez l huile et melangez encore.</div>
        </div>
        <div class="step">
            <div class="step-num">6</div>
            <div class="step-content">Laissez reposer la pate 20 a 30 minutes si vous avez le temps.</div>
        </div>
        <div class="step">
            <div class="step-num">7</div>
            <div class="step-content">Faites chauffer une poele antiadhesive a feu moyen-vif. Huilez legerement avec un papier absorbant.</div>
        </div>
        <div class="step">
            <div class="step-num">8</div>
            <div class="step-content">Versez une louche de pate et inclinez rapidement la poele pour former un disque fin.</div>
        </div>
        <div class="step">
            <div class="step-num">9</div>
            <div class="step-content">Cuisez environ 1 a 2 minutes jusqu a ce que les bords se decollent, puis retournez la crepe.</div>
        </div>
