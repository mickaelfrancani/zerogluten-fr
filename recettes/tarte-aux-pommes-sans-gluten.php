<div class="recipe-body">
    <div class="ingredients-box">
        <h3>Ingredients</h3>
        <ul>
            <li><strong>Pour la pate :</strong></li>
            <li>200 g de farine de riz blanche</li>
            <li>80 g de fecule de mais (Maizena)</li>
            <li>1 c. a cafe de gomme de xanthane</li>
            <li>1 pincee de sel</li>
            <li>100 g de beurre froid en des</li>
            <li>1 oeuf</li>
            <li>3-4 c. a soupe d eau froide</li>
            <li><strong>Pour la garniture :</strong></li>
            <li>5 pommes (Golden ou Reine des Reinettes)</li>
            <li>3 c. a soupe de sucre roux</li>
            <li>1 c. a cafe de cannelle</li>
            <li>Le jus d un demi-citron</li>
            <li>2 c. a soupe de confiture d abricot</li>
        </ul>
    </div>
    <div class="steps">
        <h3>Etapes de preparation</h3>
        <div class="step">
            <div class="step-num">1</div>
            <div class="step-content">Preparez la pate : dans un grand bol, melangez la farine de riz, la fecule de mais, la gomme de xanthane et le sel.</div>
        </div>
        <div class="step">
            <div class="step-num">2</div>
            <div class="step-content">Ajoutez le beurre froid en des et sablez du bout des doigts jusqu a obtenir une texture sableuse.</div>
        </div>
        <div class="step">
            <div class="step-num">3</div>
            <div class="step-content">Incorporez l oeuf puis ajoutez l eau froide cuillere par cuillere jusqu a former une boule de pate souple (elle sera legerement plus collante qu une pate classique, c est normal).</div>
        </div>
        <div class="step">
            <div class="step-num">4</div>
            <div class="step-content">Enveloppez la pate dans du film alimentaire et refrigerez 30 minutes.</div>
        </div>
        <div class="step">
            <div class="step-num">5</div>
            <div class="step-content">Prechauffez le four a 180°C (chaleur tournante).</div>
        </div>
        <div class="step">
            <div class="step-num">6</div>
            <div class="step-content">Etalez la pate entre deux feuilles de papier cuisson (technique indispensable car la pate sans gluten est plus fragile). Foncez un moule a tarte de 26 cm.</div>
        </div>
        <div class="step">
            <div class="step-num">7</div>
            <div class="step-content">Piquez le fond avec une fourchette. Faites precuire a blanc 10 minutes avec des billes de cuisson.</div>
        </div>
        <div class="step">
            <div class="step-num">8</div>
            <div class="step-content">Pendant ce temps, epluchez et tranchez finement les pommes. Arrosez-les du jus de citron, saupoudrez de sucre et de cannelle, melangez delicatement.</div>
        </div>
        <div class="step">
            <div class="step-num">9</div>
            <div class="step-content">Disposez les pommes en rosace sur le fond de tarte precuit.</div>
        </div>
        <div class="step">
            <div class="step-num">10</div>
            <div class="step-content">Enfournez 35 a 40 minutes jusqu a ce que les pommes soient dorees et la pate bien cuite.</div>
        </div>
        <div class="step">
            <div class="step-num">11</div>
            <div class="step-content">A la sortie du four, badigeonnez avec la confiture d abricot legerement chauffee pour un beau nappage brillant.</div>
        </div>
        <div class="step">
            <div class="step-num">12</div>
            <div class="step-content">Laissez refroidir 15 minutes avant de servir.</div>
        </div>
        <div class="tips-box">
            <h4>💡 Conseils du chef</h4>
            <p>La gomme de xanthane est essentielle dans cette recette : elle joue le role du gluten et donne de la cohesion a la pate. Vous la trouverez en magasin bio ou sur internet. Si votre pate se fissure en la manipulant, pas de souci : colmatez avec les doigts, elle se tient parfaitement a la cuisson. Pour une version encore plus gourmande, ajoutez une couche de creme d amandes sur le fond de tarte avant les pommes.</p>
        </div>
    </div>
