<div class="recipe-body">
    <div class="ingredients-box">
        <h3>Ingredients</h3>
        <ul>
            <li><strong>Pour le gratin :</strong></li>
            <li>800 g de courgettes (4 moyennes)</li>
            <li>1 oignon</li>
            <li>2 gousses d ail</li>
            <li>2 oeufs</li>
            <li>100 g de gruyere rape</li>
            <li>Sel, poivre, muscade</li>
            <li>2 c. a soupe d huile d olive</li>
            <li><strong>Pour la bechamel :</strong></li>
            <li>30 g de farine de riz</li>
            <li>30 g de beurre</li>
            <li>350 ml de lait</li>
            <li>Sel, poivre, noix de muscade</li>
        </ul>
    </div>
    <div class="steps">
        <h3>Etapes de preparation</h3>
        <div class="step">
            <div class="step-num">1</div>
            <div class="step-content">Prechauffez le four a 190°C.</div>
        </div>
        <div class="step">
            <div class="step-num">2</div>
            <div class="step-content">Preparez la bechamel : faites fondre le beurre dans une casserole, ajoutez la farine de riz et melangez 1 minute sur feu moyen.</div>
        </div>
        <div class="step">
            <div class="step-num">3</div>
            <div class="step-content">Ajoutez le lait progressivement en fouettant sans cesse. Portez a ebullition en continuant de fouetter jusqu a epaississement. Salez, poivrez, ajoutez la muscade. Reservez.</div>
        </div>
        <div class="step">
            <div class="step-num">4</div>
            <div class="step-content">Lavez et coupez les courgettes en rondelles d environ 5 mm.</div>
        </div>
        <div class="step">
            <div class="step-num">5</div>
            <div class="step-content">Emincez l oignon et hachez l ail.</div>
        </div>
        <div class="step">
            <div class="step-num">6</div>
            <div class="step-content">Dans une grande poele, faites revenir l oignon et l ail dans l huile d olive 3 minutes. Ajoutez les courgettes, salez et faites cuire 8 a 10 minutes jusqu a ce qu elles soient tendres.</div>
        </div>
        <div class="step">
            <div class="step-num">7</div>
            <div class="step-content">Hors du feu, melangez les courgettes avec la bechamel et les oeufs battus.</div>
        </div>
        <div class="step">
            <div class="step-num">8</div>
            <div class="step-content">Versez dans un plat a gratin beurre. Couvrez de gruyere rape.</div>
        </div>
        <div class="step">
            <div class="step-num">9</div>
            <div class="step-content">Enfournez 30 a 35 minutes jusqu a ce que le dessus soit dore et gratine.</div>
        </div>
        <div class="step">
            <div class="step-num">10</div>
            <div class="step-content">Laissez reposer 5 minutes avant de servir.</div>
        </div>
        <div class="tips-box">
            <h4>💡 Conseils du chef</h4>
            <p>Pour un gratin encore plus savoureux, ajoutez des des de jambon ou des lardons avec les courgettes. Vous pouvez aussi varier les fromages : la mozzarella donne un cote filant tres gourmand. Ce gratin se prepare a l avance et se rechauffe tres bien. La farine de riz donne une bechamel legerement plus dense mais tout aussi savoureuse.</p>
