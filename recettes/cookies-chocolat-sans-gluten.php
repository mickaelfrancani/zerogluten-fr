    <div class="steps">
        <h3>Etapes de preparation</h3>
        <div class="step">
            <div class="step-num">1</div>
            <div class="step-content">Prechauffez le four a 180°C. Preparez deux plaques de cuisson avec du papier cuisson.</div>
        </div>
        <div class="step">
            <div class="step-num">2</div>
            <div class="step-content">Dans un bol, melangez la farine de riz, la fecule, la gomme de xanthane, le bicarbonate et le sel. Reservez.</div>
        </div>
        <div class="step">
            <div class="step-num">3</div>
            <div class="step-content">Dans un grand bol, battez le beurre mou avec les deux sucres jusqu a obtenir un melange cremeux et clair (environ 2 a 3 minutes au fouet electrique).</div>
        </div>
        <div class="step">
            <div class="step-num">4</div>
            <div class="step-content">Ajoutez les oeufs un a un en battant bien entre chaque ajout. Incorporez la vanille.</div>
        </div>
        <div class="step">
            <div class="step-num">5</div>
            <div class="step-content">Ajoutez le melange de farines en deux fois et melangez juste assez pour incorporer (ne pas trop travailler la pate).</div>
        </div>
        <div class="step">
            <div class="step-num">6</div>
            <div class="step-content">Incorporez les pepites de chocolat a la spatule.</div>
        </div>
        <div class="step">
            <div class="step-num">7</div>
            <div class="step-content">Formez des boules de pate de la taille d une grosse noix (environ 40 g chacune) et deposez-les sur les plaques en les espacant de 5 cm.</div>
        </div>
        <div class="step">
            <div class="step-num">8</div>
            <div class="step-content">Aplatissez legerement chaque boule avec la paume.</div>
        </div>
        <div class="step">
            <div class="step-num">9</div>
            <div class="step-content">Enfournez 10 a 12 minutes : les cookies doivent paraitre encore legerement sous-cuits au centre. Ils durciront en refroidissant.</div>
        </div>
        <div class="step">
            <div class="step-num">10</div>
            <div class="step-content">Laissez refroidir 5 minutes sur la plaque avant de les deposer sur une grille.</div>
        </div>
