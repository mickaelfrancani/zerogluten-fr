        <h3>Ingredients</h3>
        <ul>
            <li>200 g de farine de riz blanche</li>
            <li>50 g de fecule de mais</li>
            <li>1/2 c. a cafe de gomme de xanthane</li>
            <li>1/2 c. a cafe de bicarbonate de soude</li>
            <li>1/2 c. a cafe de sel</li>
            <li>150 g de beurre mou (pas fondu)</li>
            <li>120 g de sucre roux (cassonade)</li>
            <li>80 g de sucre blanc</li>
            <li>2 oeufs</li>
            <li>1 c. a cafe d extrait de vanille</li>
            <li>200 g de pepites de chocolat</li>
